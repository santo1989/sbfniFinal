                                    <h3 class="page-headings" style="padding: 10px 10px">Nursing</h3>
                                    <p class="page-para">Nursing assists clients (individuals, families and communities which aggregates of people within communities) to attain physical, mental, social and spiritual well-being. Nursing involves a dynamic interaction between client and nurse aimed to promote, maintain, and restore optimal health in collaboration with other health team members. It also encourages involvement of clients and significant others in the care for self-reliance and in modifying their health behaviors. Nurses use nursing process in determining clients’ needs or problems in responses to actual or potential health problems, planning for nursing interventions, intervening, and evaluating responses. Nurses deliver community-oriented nursing care, taking into account clients’ community context. Critical thinking is the basis for nurses to apply nursing knowledge and to follow ethical and legal framework in their practice. </p>
