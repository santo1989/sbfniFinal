                                    <h3 class="page-headings"style="padding: 10px 10px">Midwifery</h3>
                                    <p class="page-para">Midwifery is care of the girl-child, the adolescent and the adult woman prior to, during, and following pregnancy. It aims to promote, maintain, and restore optimal health and views pregnancy as a normal life event. Midwife gives necessary supervision, care and advice for women during pregnancy, labor and the postpartum period. The midwife conducts deliveries on her own responsibility and cares for the newborn infant. She detects abnormal conditions in the mother and baby, procures medical assistance as necessary; and the execution of emergency measures in the absence of medical help. The midwife works in collaboration with other health care team members and/or community-based health workers in promoting the health of women and childbearing families.</p>

                                    <h3  class="page-headings"style="padding: 10px 10px">Nursing Education</h3>
                                    <p class="page-para">Nursing education is a dynamic, continuous learning process of acquiring nursing and midwifery knowledge and skills that bring about changes of student behaviors. Nursing education acquires active and life-long learning and new learning builds on previous knowledge and experience. Learning in nursing education is best achieved when student is motivated and ready to learn, where student’s dignity is respected; teaching strategies and learning experiences are carefully selected to facilitate critical thinking; and professional or clinical role modeling is offered.  </p>

                                    <h3 class="page-headings" style="padding: 10px 10px">Goal</h3>
                                    <p class="page-para">The institutional goal of this program is to produce competent nurses that will enable them for  demonstrate the competency of using knowledge-based practice for provision of quality holistic client centered nursing care to meet the needs/expectations and to promote, maintain, and restore heath of  individuals, families, and communities nationally and internationally.</p>
                                    
